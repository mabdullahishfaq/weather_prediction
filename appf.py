        "    # Option 2: Auto-generate data if file not found\n",
        "    st.warning(\"Using sample data (upload your CSV to use real data)\")\n",
        "    np.random.seed(42)\n",
        "    df = pd.DataFrame({\n",
        "        'temp_max': np.random.normal(20, 8, 1000),\n",
        "        'temp_min': np.random.normal(10, 5, 1000),\n",
        "        'wind': np.random.exponential(5, 1000),\n",
        "        'precipitation': np.random.exponential(2, 1000)\n",
        "    })\n",
        "\n",
        "# Show data\n",
        "st.subheader(\"📊 Your Data\")\n",
        "st.dataframe(df.head())\n",
