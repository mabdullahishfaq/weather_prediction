        "# ==========================================\n",
        "# SELECT WHAT TO PREDICT\n",
        "# ==========================================\n",
        "# Let user choose which column to predict\n",
        "all_columns = df.columns.tolist()\n",
        "target = st.selectbox(\"What do you want to predict?\", all_columns, index=len(all_columns)-1)\n",
        "\n",
        "# Select features (all other columns)\n",
        "features = [col for col in all_columns if col != target]\n",
        "\n",
        "st.write(f\"**Predicting:** {target}\")\n",
        "st.write(f\"**Using these factors:** {', '.join(features)}\")\n",
