        "# ==========================================\n",
        "# CREATE NEURAL NETWORK\n",
        "# ==========================================\n",
        "model = Sequential([\n",
        "    Dense(64, activation='relu', input_shape=(len(features),)),\n",
        "    Dropout(0.2),\n",
        "    Dense(32, activation='relu'),\n",
        "    Dense(1)\n",
        "])\n",
        "\n",
        "model.compile(optimizer='adam', loss='mse', metrics=['mae'])\n",
        "model.summary()\n",
        "\n",
        "# ==========================================\n",
        "# TRAIN BUTTON\n",
        "# ==========================================\n",
        "if st.button(\"🚀 Train Model\"):\n",
        "    with st.spinner(\"Training...\"):\n",
        "        # Train\n",
        "        history = model.fit(X_train, y_train, epochs=20, batch_size=32,\n",
        "                           validation_split=0.1, verbose=0)\n",
        "\n",
        "        # Evaluate\n",
        "        loss, mae = model.evaluate(X_test, y_test, verbose=0)\n",
        "\n",
        "        # Show results\n",
        "        st.success(f\"✅ Training Complete! Error: {mae:.2f}\")\n",
        "\n",
        "        # Store model\n",
        "        st.session_state['model'] = model\n",
        "        st.session_state['scaler'] = scaler\n",
        "        st.session_state['mae'] = mae\n",
        "\n",
        "        # Plot training progress\n",
        "        fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(12, 4))\n",
        "\n",
        "        ax1.plot(history.history['loss'], label='Training Loss')\n",
        "        ax1.plot(history.history['val_loss'], label='Validation Loss')\n",
        "        ax1.set_xlabel('Epoch')\n",
        "        ax1.set_ylabel('Loss')\n",
        "        ax1.set_title('Training Progress')\n",
        "        ax1.legend()\n",
        "        ax1.grid(True)\n",
        "\n",
        "        # Plot predictions\n",
        "        predictions = model.predict(X_test)\n",
        "        ax2.scatter(y_test, predictions, alpha=0.5)\n",
        "        ax2.plot([y_test.min(), y_test.max()], [y_test.min(), y_test.max()], 'r--')\n",
        "        ax2.set_xlabel('Actual')\n",
        "        ax2.set_ylabel('Predicted')\n",
        "        ax2.set_title('Predictions vs Actual')\n",
        "        ax2.grid(True)\n",
        "\n",
        "        st.pyplot(fig)\n",
        "\n",
        "# ==========================================\n",
        "# MAKE PREDICTIONS\n",
        "# ==========================================\n",
        "st.subheader(\"🔮 Make a Prediction\")\n",
        "\n",
        "if 'model' in st.session_state:\n",
        "    # Create input boxes for each feature\n",
        "    user_inputs = []\n",
        "    cols = st.columns(len(features))\n",
        "\n",
        "    for i, feature in enumerate(features):\n",
        "        with cols[i]:\n",
        "            # Get average value as default\n",
        "            default = float(df[feature].mean())\n",
        "            val = st.number_input(f\"{feature}\", value=default)\n",
        "            user_inputs.append(val)\n",
        "\n",
        "    if st.button(\"Predict Now\"):\n",
        "        # Make prediction\n",
        "        input_data = scaler.transform([user_inputs])\n",
        "        prediction = st.session_state['model'].predict(input_data)[0][0]\n",
        "\n",
        "        # Show result\n",
        "        st.markdown(f\"\"\"\n",
        "        <div style=\"text-align: center; padding: 20px; background-color: #1e88e5; border-radius: 10px;\">\n",
        "            <h2 style=\"color: white;\">Predicted {target}</h2>\n",
        "            <h1 style=\"font-size: 48px; color: white;\">{prediction:.1f}</h1>\n",
        "            <p style=\"color: white;\">Model Error: ± {st.session_state['mae']:.2f}</p>\n",
        "        </div>\n",
