# ==========================================
# PART 1: GENERATE LARGE DATASET (sample.csv)
# ==========================================
print("Generating large dataset 'sample.csv'...")

def generate_weather_data(num_rows=50000):
    np.random.seed(42)
    days = np.arange(1, 366)
    seasonal_component = 15 * np.sin(2 * np.pi * (days - 200) / 365)

    data = []

    for _ in range(num_rows):
        day_idx = np.random.randint(0, 365)
        base_temp = 20 + seasonal_component[day_idx]

        temperature_variation = np.random.normal(0, 5)
        current_max_temp = base_temp + temperature_variation
        current_min_temp = current_max_temp - np.random.uniform(5, 10)

        pressure = 1013 - (current_max_temp - 20) * 0.5 + np.random.normal(0, 2)

        humidity = 50 + (25 - current_max_temp) * 2 + np.random.normal(0, 10)
        humidity = np.clip(humidity, 0, 100)

        wind_speed = np.random.exponential(5)

        next_day_max_temp = current_max_temp + np.random.uniform(-3, 3) - 0.5

        data.append([
            round(humidity, 1),
            round(wind_speed, 1),
            round(pressure, 1),
            round(current_max_temp, 1),
            round(current_min_temp, 1),
            round(next_day_max_temp, 1)
        ])

    df = pd.DataFrame(data, columns=[
        'Humidity', 'WindSpeed', 'Pressure',
        'CurrentMaxTemp', 'CurrentMinTemp', 'NextDayMaxTemp'
    ])
    return df

# Generate the file
df = generate_weather_data(50000)
df.to_csv('sample1.csv', index=False)
print(f"Success! Generated 'sample.csv' with {len(df)} rows.")
print("-" * 50)

# ==========================================
# PART 2: BUILD AND TRAIN THE ANN
# ==========================================

print("Loading dataset...")
dataset = pd.read_csv("sample1.csv")
print(dataset.head())

X = dataset.iloc[:, :-1].values
Y = dataset.iloc[:, -1].values

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=42)

scaler = StandardScaler()
X_train = scaler.fit_transform(X_train)
X_test = scaler.transform(X_test)

# Build the model
model = Sequential([
    Dense(64, activation='relu', input_shape=(X_train.shape[1],)),
    Dropout(0.2),
    Dense(64, activation='relu'),
    Dropout(0.2),
    Dense(32, activation='relu'),
    Dense(1)
])

model.compile(
    optimizer='adam',
    loss='mean_squared_error',
    metrics=['mae']
)

model.summary()

print("\nTraining ANN... Please wait.")
history = model.fit(
    X_train, Y_train,
    batch_size=32,
    epochs=20,
    validation_split=0.1,
    verbose=1
)

print("\nEvaluating model...")
loss, mae = model.evaluate(X_test, Y_test)
print(f"Mean Absolute Error on Test Set: {mae:.2f} degrees Celsius")

# Make predictions
Y_pred = model.predict(X_test)

# Plot results
plt.figure(figsize=(10, 6))
plt.scatter(Y_test, Y_pred, color='red', alpha=0.5, label='Predictions')
plt.plot([Y_test.min(), Y_test.max()], [Y_test.min(), Y_test.max()], 'b--', lw=2, label='Perfect Prediction')
plt.xlabel('Actual Next Day Max Temp (°C)')
plt.ylabel('Predicted Next Day Max Temp (°C)')
plt.title('Weather Prediction: ANN Performance')
plt.legend()
plt.grid(True)
plt.show()

# Save the model
model.save('weather_ann_model.h5')
print("\nModel saved as 'weather_ann_model.h5'")

